//Creating the array of the characters and printing all of them.
public class ArrayOfCharacter {

        public static void main(String[] args) {  
            char[] arrayChar = {'h', 'e', 'j', 'y', 'k','a','c','d','s'};  
            System.out.println("The array characters are as follows :");
           for (int noOfCharacter = 0; noOfCharacter < arrayChar.length; noOfCharacter++) {
                System.out.println(arrayChar[noOfCharacter]);
           }
        }  
}
